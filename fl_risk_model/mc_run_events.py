            water_total=_numcol("water_total_usd", 0.0),
            total_damage=_numcol("wind_total_usd", 0.0) + _numcol("water_total_usd", 0.0),

            # six-bucket gross decomposition (absolute USD)
            wind_ins_priv=_numcol("wind_insured_private_usd", 0.0),     # (a)
