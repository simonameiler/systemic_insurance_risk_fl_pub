def _load_event_df(path: Path) -> pd.DataFrame:
    df = pd.read_csv(path)
    low = {c.lower(): c for c in df.columns}
    if {"countyfp", "county_name", "value"}.issubset({c.lower() for c in df.columns}):
        county_col = low["county_name"]; total_col = low["value"]
        countyfp_col = low["countyfp"]  # Keep FIPS for county redistribution
    else:
        county_col = low.get("county") or low.get("county_name") or low.get("name")
        total_col  = (low.get("totallossusd") or low.get("total_loss_usd") or low.get("loss_total_usd") or
                      low.get("loss_usd") or low.get("value") or low.get("loss"))
        countyfp_col = low.get("countyfp") or low.get("fips") or low.get("county_fips")
        if county_col is None or total_col is None:
            raise KeyError(f"{path.name}: need county+loss columns. Present: {list(df.columns)}")
    
    # Build output with county and loss columns, plus FIPS if available
    cols_to_keep = [county_col, total_col]
    out_col_names = ["County", "TotalLossUSD"]
    if countyfp_col is not None:
        cols_to_keep.append(countyfp_col)
        out_col_names.append("countyfp")
    
    out = df[cols_to_keep].copy()
    out.columns = out_col_names
    out["County"] = (
        out["County"].astype(str)
            .str.replace(r"\s+County$", "", regex=True)
            .str.strip()
    )
    out = out[out["County"].notna() & (out["County"].str.lower() != "nan") & (out["County"] != "")]
    out["TotalLossUSD"] = numseries(out["TotalLossUSD"])
    return out


def _sample_wind_share(event_key: str, rng: np.random.Generator, use_climate_scaling: bool = False) -> float:
    """
    Sample wind share for a single event.
    
    Supports multiple sampling modes:
    - "beta": Beta distribution with mean and concentration (recommended)
    - "uniform": Uniform over bounds (legacy)
    - "triangular": Triangular over bounds (legacy)
    
    Args:
        event_key: Event identifier (e.g., "1926255N15314")
        rng: Random number generator
        use_climate_scaling: If True, use future climate parameters (empirically-adjusted)
    
    Returns:
        Wind share (0-1)
    """
    if DEFAULT_WIND_SHARE_MODE == "beta":
        # Select parameter dictionary based on climate scenario
        if use_climate_scaling:
            params = EVENT_WIND_SHARE_PARAMS_FUTURE.get(event_key)
            # Fall back to present-day if future not defined
            if params is None:
                params = EVENT_WIND_SHARE_PARAMS.get(event_key)
        else:
            params = EVENT_WIND_SHARE_PARAMS.get(event_key)
        
        if params is None:
            # Fall back to default (use future default if climate scaling enabled)
            mean = DEFAULT_WIND_SHARE_MEAN_FUTURE if use_climate_scaling else DEFAULT_WIND_SHARE_MEAN
            concentration = DEFAULT_WIND_SHARE_CONCENTRATION
        else:
            mean = params["mean"]
            concentration = params["concentration"]
        
        # Convert mean + concentration to alpha, beta parameters
        alpha = mean * concentration
        beta_param = (1 - mean) * concentration
        return float(rng.beta(alpha, beta_param))
    
    else:
        # Legacy modes: uniform or triangular (no climate adjustment)
        a, b = EVENT_WIND_SHARE_BOUNDS.get(event_key, DEFAULT_WIND_SHARE_BOUNDS)
        a, b = (min(a, b), max(a, b))
        if DEFAULT_WIND_SHARE_MODE == "triangular":
            mode = (2*a + b)/3.0
            return float(rng.triangular(a, mode, b))
        return float(rng.uniform(a, b))


