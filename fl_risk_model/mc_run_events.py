"""
mc_run_events.py - Monte Carlo event simulator for Florida insurance risk model
---------------------------------------------------------------------------------

Runs Monte Carlo simulations of hurricane scenarios through the Florida
insurance system, tracking losses, reinsurance recoveries, and systemic
risk propagation.

Key Features
------------
- Single-event scenarios (e.g., Great Miami, Andrew, Irma)
- Multi-event composite scenarios with sequential exposure degradation
- Wind/water damage splits sampled from event-specific priors (Beta distributions)
- County-specific wind/water redistribution (optional, preserves overall split)
- Full insurance waterfall: primary insurers -> FHCF -> FIGA -> Citizens
- NFIP flood losses with federal backstop tracking

Wind/Water Split Modes
----------------------
1. UNIFORM (default): Same wind share applied to all counties
2. COUNTY-SPECIFIC: Spatially heterogeneous splits based on NCEP climatology
   - Preserves overall wind share from Beta distribution
   - Adds county-specific deviations from NCEP p95 mean
   - Example: 70% baseline + Miami-Dade deviation (+4.3%) = 74.3% for Miami-Dade
   - Enable with: USE_COUNTY_REDISTRIBUTION = True in config

Composite Scenarios
-------------------
For multi-storm scenarios (e.g., two hurricanes in the same year), uses
pre-computed composite damage files that account for sequential exposure
degradation. These are generated by:
    python scripts/hazard/compute_sequential_events.py

Usage
-----
    python mc_run_events.py --n_iter 200 --seed 42 --out results/mc_runs
"""
