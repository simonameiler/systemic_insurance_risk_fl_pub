def _combine_events_for_scenario(scenario_name: str, event_stems: List[str], rng: np.random.Generator):
    """
    Load event damage and split into wind/water.
    
    For composite scenarios (multi-event with sequential degradation),
    loads pre-computed composite CSV directly and uses a fixed effective
    wind share (weighted average of component events).
    
    For single-event or naive multi-event, loads individual event CSVs
    and naively sums them (no degradation).
    """
    # Check if this is a composite scenario
    if len(event_stems) == 1 and event_stems[0] == "COMPOSITE":
        if scenario_name not in COMPOSITE_SCENARIOS:
            raise ValueError(
                f"Scenario '{scenario_name}' marked as COMPOSITE but not found in COMPOSITE_SCENARIOS. "
                f"Available: {list(COMPOSITE_SCENARIOS.keys())}"
            )
        
        # Load pre-computed composite file (already has sequential degradation)
        composite_file, composite_wind_share = COMPOSITE_SCENARIOS[scenario_name]
        p = EVENT_REPORTS_DIR / composite_file
        if not p.exists():
            raise FileNotFoundError(
                f"Composite file not found: {p}\n"
                f"Generate it using: python scripts/hazard/compute_sequential_events.py"
            )
        
        base = _load_event_df(p)
        
        # Sample wind share for composite scenarios
        # Use Beta distribution centered at weighted average with tighter concentration
        # Select parameter dictionary based on climate scenario (wind/water attribution)
        use_future_wind_share = getattr(cfg, "USE_FUTURE_WIND_SHARE", False)
        if use_future_wind_share and scenario_name in COMPOSITE_WIND_SHARE_PARAMS_FUTURE:
            params = COMPOSITE_WIND_SHARE_PARAMS_FUTURE[scenario_name]
        elif scenario_name in COMPOSITE_WIND_SHARE_PARAMS:
            params = COMPOSITE_WIND_SHARE_PARAMS[scenario_name]
        else:
            # Fallback: use the stored wind share value directly (from tuple)
            # This shouldn't happen if config is properly set up
            params = None
        
        if params is not None:
            mean = params["mean"]
            concentration = params["concentration"]
            
            # Convert to Beta parameters
            alpha = mean * concentration
            beta_param = (1 - mean) * concentration
            w_share = float(rng.beta(alpha, beta_param))
        else:
            w_share = composite_wind_share
        
        sampled = {scenario_name: w_share}
        
        # Apply county-specific redistribution if enabled
        if USE_COUNTY_REDISTRIBUTION:
            use_future_wind_share = getattr(cfg, "USE_FUTURE_WIND_SHARE", False)
            wind_df, water_df = _apply_county_redistribution(base, w_share, rng, use_future_climate=use_future_wind_share)
        else:
            # Uniform split across all counties
            wind_df = base.assign(WindDamageUSD=base["TotalLossUSD"] * w_share)[["County", "WindDamageUSD"]]
            water_df = base.assign(WaterDamageUSD=base["TotalLossUSD"] * (1.0 - w_share))[["County", "WaterDamageUSD"]]
        
        return wind_df, water_df, sampled
    
    # Original logic: naive summation for single events or manual multi-event
    wind_parts, water_parts, sampled = [], [], {}
    
    # Use synthetic event directory if in stochastic mode
    # Read from cfg dynamically to support Emanuel events (where dir is set at runtime)
    if _USE_STOCHASTIC_EVENTS:
        event_dir = Path(getattr(cfg, "SYNTHETIC_EVENT_DIR", DATA_DIR / "hazard" / "synthetic"))
    else:
        event_dir = EVENT_REPORTS_DIR
    
    for stem in event_stems:
        p = event_dir / f"{stem}.csv"
        if not p.exists():
            raise FileNotFoundError(f"Missing event CSV: {p}")
        base = _load_event_df(p)
        
        # Sample wind share (use future parameters if future wind share enabled)
        use_future_wind_share = getattr(cfg, "USE_FUTURE_WIND_SHARE", False)
        w_share = _sample_wind_share(stem, rng, use_climate_scaling=use_future_wind_share)
        sampled[stem] = w_share
        
        # Apply county-specific redistribution if enabled
        if USE_COUNTY_REDISTRIBUTION:
            wind_part, water_part = _apply_county_redistribution(base, w_share, rng, use_future_climate=use_future_wind_share)
        else:
            # Uniform split across all counties
            wind_part = base.assign(WindDamageUSD=base["TotalLossUSD"] * w_share)[["County","WindDamageUSD"]]
            water_part = base.assign(WaterDamageUSD=base["TotalLossUSD"] * (1.0 - w_share))[["County","WaterDamageUSD"]]
        
        wind_parts.append(wind_part)
        water_parts.append(water_part)
    
    wind_df = pd.concat(wind_parts).groupby("County", as_index=False).sum()
    water_df = pd.concat(water_parts).groupby("County", as_index=False).sum()
    return wind_df, water_df, sampled


def run_one_iteration(scenario_name: str,
                      event_stems: List[str],
                      rng: np.random.Generator,
                      common_inputs: tuple,
                      do_flood: bool = DO_FLOOD,
                      surplus_year: int | None = YEAR,
                      policy_scenario_config: dict | None = None,
                      group_support_eligibility_threshold: float = 10.0) -> dict:
    """
    Run one Monte Carlo iteration with optional policy scenario.
    
    Parameters:
    -----------
    scenario_name : str
        Hazard scenario name (e.g., "great_miami", "andrew")
    event_stems : List[str]
        Event IDs to combine
    rng : np.random.Generator
        Random number generator
    common_inputs : tuple
        Pre-loaded common data (FHCF, market share, etc.)
    do_flood : bool
        Whether to include flood modeling
    surplus_year : int | None
        Year for surplus data
    policy_scenario_config : dict | None
        Optional policy scenario configuration
    group_support_eligibility_threshold : float, default=10.0
        Group-to-Entity surplus ratio threshold for intragroup capital support eligibility
    surplus_year : int | None
        Year for surplus data
    policy_scenario_config : dict | None
        Policy intervention configuration with {"type": str, "params": dict}
        If None, runs baseline (no intervention)
    
    Returns:
    --------
    dict : Results dictionary with all metrics
    """

    wind_df, water_df, sampled = _combine_events_for_scenario(scenario_name, event_stems, rng)

    # Expose sampled wind shares to any branch code that reads cfg at runtime
    cfg.RUNTIME_WIND_SHARE_OVERRIDES = dict(sampled)

    # Totals for audit (BEFORE any scenario modifications)
    wind_total = numsum(wind_df["WindDamageUSD"])
    water_total = numsum(water_df["WaterDamageUSD"])

    # =============================================================================
    # SCENARIO APPLICATION: Building codes (BEFORE carveout)
    # =============================================================================
    # CRITICAL: Apply building codes BEFORE carveout so that underinsured/uninsured
    # calculations use the reduced wind damage, not the gross damage
    
    building_codes_diag = None
    if policy_scenario_config is not None and policy_scenario_config.get("type") == "building_codes":
        scenario_params = policy_scenario_config.get("params", {})
        
        # Apply loss reduction to gross wind damage
        wind_df_bc = wind_df.rename(columns={"WindDamageUSD": "GrossWindLossUSD"})
        wind_df_bc, building_codes_diag_wind = scenarios.apply_building_code_scenario(
            loss_df=wind_df_bc,
            hazard_type="wind",
            **scenario_params
        )
        wind_df = wind_df_bc.rename(columns={"GrossWindLossUSD": "WindDamageUSD"})
        
        # Apply loss reduction to gross flood damage
        water_df_bc = water_df.rename(columns={"WaterDamageUSD": "GrossFloodLossUSD"})
        water_df_bc, building_codes_diag_flood = scenarios.apply_building_code_scenario(
            loss_df=water_df_bc,
            hazard_type="flood",
            **scenario_params
        )
        water_df = water_df_bc.rename(columns={"GrossFloodLossUSD": "WaterDamageUSD"})
        
        # Combine diagnostics
        building_codes_diag = {
            "wind": building_codes_diag_wind,
            "flood": building_codes_diag_flood,
        }
    
    # IMPORTANT: Inject damage loaders AFTER building codes is applied
    # so that runner.load_wind_damage() gets the reduced wind damage
    _inject_damage_loaders(wind_df, water_df)
    
    fhcf_county_df, mshare, county_xwalk, cit_cap, nfip_claims_df, nfip_exposure_df = common_inputs

    # Full scenario pipeline with optional policy intervention
    # Derive a per-iteration seed from rng so that TIV sampling varies across
    # iterations.  (Previously used the constant cfg.RNG_SEED, making every
    # iteration's TIV perturbation identical.)
    iter_seed = int(rng.integers(0, 2**31))
    result = run_one_scenario(
        storm_name=scenario_name,
        fhcf_county_df=fhcf_county_df,
        market_share_df=mshare,
        county_xwalk=county_xwalk,
        citizens_capital_row=cit_cap,
        nfip_claims_df=nfip_claims_df,
        nfip_exposure_df=nfip_exposure_df,
        seed=iter_seed,
        do_flood=do_flood,
        surplus_year=surplus_year,
        scenario_config=policy_scenario_config,  # NEW: Pass policy scenario
        group_support_eligibility_threshold=group_support_eligibility_threshold,  # Pass through threshold
    )

    # 2) Pull NFIP outputs from runner result
    flood_df = result.get("flood_nfip_recovery")
    to_num = lambda s: numseries(s)

    # Backward-compatible keys (accept either *_usd or *_total)
    nfip_borrowed   = float(result.get("nfip_borrowed_usd",   result.get("nfip_borrowed_total", 0.0)))
    nfip_claims_paid = float(result.get("nfip_claims_paid_usd", result.get("nfip_claims_paid_total", 0.0)))
    nfip_pool_used   = float(result.get("nfip_pool_used_usd",   result.get("nfip_pool_used_total", 0.0)))

    # Aggregate flood attribution from DF
    if do_flood and isinstance(flood_df, pd.DataFrame) and not flood_df.empty:
        uninsured_flood_usd       = float(to_num(flood_df.get("UninsuredFloodUSD")).sum())
        insured_and_paid_usd      = float(to_num(flood_df.get("NFIP_PayoutUSD")).sum())
        policyholder_shortfall_usd = float(to_num(flood_df.get("PolicyholderShortfallUSD")).sum())
        # If you report two buckets only, keep underinsured separate only if you need it for summary rows:
        underinsured_flood = policyholder_shortfall_usd  # optional, if you want a proxy bucket
    else:
        uninsured_flood_usd = insured_and_paid_usd = policyholder_shortfall_usd = 0.0
        underinsured_flood  = 0.0

    # # Shares of total water loss
    # water_total = float(to_num(water_df["WaterDamageUSD"]).sum())
    # uninsured_flood_share    = (uninsured_flood_usd / water_total) if water_total > 0 else 0.0
    # underinsured_flood_share = (underinsured_flood  / water_total) if water_total > 0 else 0.0

    # --- Six-bucket gross decomposition that adds back to total damage ---

    # Wind buckets - extract from runner's carveout result
    wind_insured_private_usd  = float(result.get("insured_private_wind_pre_usd", 0.0) or 0.0)
    wind_insured_citizens_usd = float(result.get("insured_citizens_wind_pre_usd", 0.0) or 0.0)
    carved_wind = result.get("carveout_wind_county")
    if isinstance(carved_wind, pd.DataFrame) and not carved_wind.empty:
        wind_underinsured_usd = float(numseries(carved_wind.get("UnderinsuredWindUSD", 0)).sum())
        wind_uninsured_usd    = float(numseries(carved_wind.get("UninsuredWindUSD", 0)).sum())
    else:
        wind_underinsured_usd = 0.0
        wind_uninsured_usd    = 0.0

    # Flood buckets (from runner’s NFIP view)
    flood_df = result.get("flood_nfip_recovery")
    to_num = lambda s: numseries(s)
    if isinstance(flood_df, pd.DataFrame) and not flood_df.empty:
        flood_insured_capped_usd = float(to_num(flood_df.get("InsuredFloodCappedUSD")).sum())
        flood_underinsured_cap_usd = float(to_num(flood_df.get("UnderinsuredFloodUSD")).sum())
        flood_uninsured_usd        = float(to_num(flood_df.get("UninsuredFloodUSD")).sum())
    else:
        flood_insured_capped_usd = 0.0
        flood_underinsured_cap_usd = 0.0
        flood_uninsured_usd = 0.0

    flood_un_derinsured_usd = flood_underinsured_cap_usd + flood_uninsured_usd

    # Optional shares (of total damage)
    total_damage_usd = float(wind_total + water_total)
    share = (lambda x: (x / total_damage_usd) * 100.0 if total_damage_usd > 0 else 0.0)

    # Identity check (tolerant)
    recon = (wind_insured_private_usd + wind_insured_citizens_usd + wind_underinsured_usd + wind_uninsured_usd
            + flood_insured_capped_usd + flood_un_derinsured_usd)
    if abs(recon - total_damage_usd) > max(1e-6 * max(total_damage_usd, 1.0), 1.0):
        dbg(f"[warn] gross partition mismatch: recon={recon:,.0f} vs total={total_damage_usd:,.0f}")

    # Add to the output dict you’re already returning
    row_updates = {
        # Wind gross decomposition
        "wind_insured_private_usd":  wind_insured_private_usd,     # (a)
        "wind_insured_citizens_usd": wind_insured_citizens_usd,    # (b)
        "wind_underinsured_usd":     wind_underinsured_usd,        # (c)
        "wind_uninsured_usd":        wind_uninsured_usd,           # (d)

        # Flood gross decomposition
        "flood_insured_capped_usd":  flood_insured_capped_usd,     # (e)
        "flood_un_derinsured_usd":   flood_un_derinsured_usd,      # (f)

        # (Optional) Shares of total damage for each bucket
        "wind_insured_private_pct":  share(wind_insured_private_usd),
        "wind_insured_citizens_pct": share(wind_insured_citizens_usd),
        "wind_underinsured_pct":     share(wind_underinsured_usd),
        "wind_uninsured_pct":        share(wind_uninsured_usd),
        "flood_insured_capped_pct":  share(flood_insured_capped_usd),
        "flood_un_derinsured_pct":   share(flood_un_derinsured_usd),
    }

    # Force NFIP BORROW mode
    try:
        if getattr(cfg, "NFIP_CAPITAL_MODE", "UNLIMITED") != "BORROW":
            cfg.NFIP_CAPITAL_MODE = "BORROW"
            cfg.NFIP_BORROW_ENABLED = True
    except Exception:
        pass


    # Capital frames
    cap_post = result.get("capital_post", pd.DataFrame())
    cap_grp  = result.get("capital_with_groups", pd.DataFrame())

    # Defaults: pre (post-loss) and post (after group support)
    defaults_pre = 0
    default_companies_pre: List[str] = []
    if isinstance(cap_post, pd.DataFrame) and not cap_post.empty:
        pre_col = "EndingSurplusUSD" if "EndingSurplusUSD" in cap_post.columns else ("EndingSurplus" if "EndingSurplus" in cap_post.columns else None)
        if pre_col:
            mask_pre = numseries(cap_post[pre_col]) < 0
            defaults_pre = int(mask_pre.sum())
            if "Company" in cap_post.columns:
                default_companies_pre = cap_post.loc[mask_pre, "Company"].astype(str).tolist()

    defaults_post = 0
    default_companies_post: List[str] = []
    if isinstance(cap_grp, pd.DataFrame) and not cap_grp.empty:
        if "DefaultFlag" in cap_grp.columns:
            mask_post = numseries(cap_grp["DefaultFlag"], default=0).astype(bool)
        else:
            endcol = next((c for c in ["AdjustedSurplusUSD","EndingSurplusUSD","EndingSurplus"] if c in cap_grp.columns), None)
            mask_post = (numseries(cap_grp[endcol]) < 0) if endcol else pd.Series([], dtype=bool)
        defaults_post = int(mask_post.sum())
        if "Company" in cap_grp.columns and len(mask_post) == len(cap_grp):
            default_companies_post = cap_grp.loc[mask_post, "Company"].astype(str).tolist()

    rescued = max(defaults_pre - defaults_post, 0)

    # Public backstops (FIGA/Citizens)
    figa = result.get("figa_result") or {}
    citizens = result.get("citizens_assessments") or {}

    # Citizens: pull tier amounts + capacities directly
    citizens_tier1_usd  = float((citizens.get("tier1_amount", 0.0) or 0.0))
    citizens_tier2_usd  = float((citizens.get("tier2_amount", 0.0) or 0.0))
    citizens_tier1_cap  = float((citizens.get("tier1_capacity", 0.0) or 0.0))
    citizens_tier2_cap  = float((citizens.get("tier2_capacity", 0.0) or 0.0))
    citizens_residual   = float((citizens.get("residual_deficit", 0.0) or 0.0))

    figa_residual = float((figa.get("residual_deficit", 0.0) or 0.0))
    citizens_residual = float((citizens.get("residual_deficit", 0.0) or 0.0))

    figa_collected = 0.0
    df_assess = figa.get("assessments")
    if isinstance(df_assess, pd.DataFrame) and not df_assess.empty:
        col = next((c for c in ["FIGA_AssessmentUSD","assessment_usd","AssessUSD"] if c in df_assess.columns), None)
        if col:
            figa_collected = numsum(df_assess[col])

    defaulted_premium_base_pct = float(result.get("defaulted_premium_base_pct", 0.0) or 0.0)
    largest_entity_deficit_usd = float(result.get("largest_entity_deficit_usd", 0.0) or 0.0)

    # Group shortfall diagnostics (from capital_with_groups)
    diag_group_shortfall_total = 0.0
    diag_groups_with_shortfall = 0
    diag_groups_fully_funded_share = 0.0

    if isinstance(cap_grp, pd.DataFrame) and not cap_grp.empty \
    and {"GroupID", "DiagGroupShortfallUSD"}.issubset(cap_grp.columns):
        # One row per group to avoid double-counting
        gshort = (cap_grp[["GroupID", "DiagGroupShortfallUSD"]]
                .drop_duplicates("GroupID")
                .copy())
        s = numseries(gshort["DiagGroupShortfallUSD"])
        diag_group_shortfall_total = float(s.sum())
        diag_groups_with_shortfall = int((s > 0).sum())
        # share of groups with zero shortfall (fully funded)
        diag_groups_fully_funded_share = float((s <= 0).mean())

    # NFIP capital summary (robust fallbacks)
    nf = result.get("nfip_capital_summary") or {}
    nfip_claims_paid = float(
        nf.get("paid_total")
        or result.get("nfip_claims_paid_total")
        or result.get("nfip_claims_paid_usd")
        or (
            result.get("flood_nfip_recovery")["NFIP_PayoutUSD"].sum()
            if isinstance(result.get("flood_nfip_recovery"), pd.DataFrame)
            and "NFIP_PayoutUSD" in result.get("flood_nfip_recovery").columns
            else 0.0
        )
        or 0.0
    )
    nfip_pool_used = float(
        nf.get("pool_used")
        or result.get("nfip_pool_used_total")
        or result.get("nfip_pool_used_usd")
        or 0.0
    )
    nfip_borrowed = float(
        nf.get("borrowed")
        or result.get("nfip_borrowed_total")
        or result.get("nfip_borrowed_usd")
        or 0.0
    )

