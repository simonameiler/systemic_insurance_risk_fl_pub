def numseries(x, default=0.0):
    if x is None:
        return pd.Series([default], dtype="float64")
    if isinstance(x, (int, float, np.integer, np.floating)):
        return pd.Series([x], dtype="float64").fillna(default)
    try:
        s = pd.Series(x)
        return pd.to_numeric(s, errors="coerce").fillna(default)
    except Exception:
        return pd.Series([default], dtype="float64")

def numsum(x) -> float:
    return float(numseries(x).sum())

def numval(x, default=0.0) -> float:
    s = numseries(x, default=default)
    return float(s.iloc[0] if len(s) else default)



def _safe_sum(obj) -> float:
    try:
        if isinstance(obj, pd.DataFrame):
            # sum all numeric entries in the frame
            vals = obj.select_dtypes(include=[np.number]).to_numpy().ravel()
            return numsum(vals)
        if isinstance(obj, pd.Series):
            return numsum(obj)
        if isinstance(obj, dict):
            # sum over dict values
            return numsum(list(obj.values()))
        if np.isscalar(obj):
            return float(obj)
        # generic fallback
        return numsum(obj)
    except Exception:
        return 0.0


def _sum_col_robust(container, *candidate_names: str) -> float:
    try:
        if isinstance(container, pd.DataFrame):
            for name in candidate_names:
                if name in container.columns:
                    return numsum(container[name])
            return 0.0
        elif isinstance(container, pd.Series):
            # if the Series itself is the desired column
            if container.name in candidate_names:
                return numsum(container)
            return _safe_sum(container)
        elif isinstance(container, dict):
            for name in candidate_names:
                if name in container:
                    return _safe_sum(container[name])
            return 0.0
        else:
            return _safe_sum(container)
    except Exception:
        return 0.0

def _preflight_events():
    missing = []
    
    # Check individual event files
    for scen, stems in SCENARIOS.items():
        if stems == ["COMPOSITE"]:
            # This is a composite scenario, check the composite file instead
            if scen in COMPOSITE_SCENARIOS:
                composite_file, _ = COMPOSITE_SCENARIOS[scen]
                p = EVENT_REPORTS_DIR / composite_file
                if not p.exists():
                    missing.append(f"{p} (composite for scenario '{scen}')")
        else:
            # Regular scenario with individual event files
            for stem in stems:
                p = EVENT_REPORTS_DIR / f"{stem}.csv"
                if not p.exists():
                    missing.append(str(p))
    
    if missing:
        raise FileNotFoundError(
            "Missing event CSV(s):\n  " + "\n  ".join(missing) +
            "\n\nFor individual events: Set cfg.EVENT_REPORTS_DIR to point to your hazard CSVs."
            "\nFor composite scenarios: Run python scripts/hazard/compute_sequential_events.py"
        )


